namespace Riftbourne.Items
{
    /// <summary>
    /// Enumeration of item rarity levels.
    /// Used for visual distinction and value scaling.
    /// </summary>
    public enum ItemRarity
    {
        Common,      // Basic items, most common
        Uncommon,    // Slightly better than common
        Rare,        // Uncommon but valuable
        Epic,        // Very rare and powerful
        Legendary    // Extremely rare, best items
    }
}
